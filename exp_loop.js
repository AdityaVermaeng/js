//javascript program to illustrate for loop
for (let i = 0; i < 5; i++) {
    console.log("Iteration:", i);
}




//javascript program to illustrate while loop

let j = 0;
while (j < 5) {
    console.log("Iteration:", j);
    j++;
}


//javascript program to illustrate do - while loop


let i = 0;
do {
    console.log("Iteration:", i);
    i++;
} while (i < 5);