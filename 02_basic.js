// Arithmetic operators
let x = 10;
let y = 5;
let sum = x + y; // Addition
let difference = x - y; // Subtraction
let product = x * y; // Multiplication
let quotient = x / y; // Division
let remainder = x % y; // Modulus

// Assignment operators
let a = 10;
a += 5; // Equivalent to: a = a + 5
a -= 3; // Equivalent to: a = a - 3
a *= 2; // Equivalent to: a = a * 2
a /= 4; // Equivalent to: a = a / 4
a %= 2; // Equivalent to: a = a % 2

// Comparison operators
let b = 5;
let c = 10;
let isEqual = b == c; // Equality
let isNotEqual = b != c; // Inequality
let isGreater = b > c; // Greater than
let isLess = b < c; // Less than
let isGreaterOrEqual = b >= c; // Greater than or equal to
let isLessOrEqual = b <= c; // Less than or equal to

// Logical operators
let p = true;
let q = false;
let andResult = p && q; // Logical AND
let orResult = p || q; // Logical OR
let notResult = !p; // Logical NOT